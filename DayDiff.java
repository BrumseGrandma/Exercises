public class DayDiff
{
	public static void main(String[] args) 
	{
		double Mon = 21.5;
		double Tue = 23.7;
		double Wed = 19.6;
		double Thu = 22.5;
		double Fri = 25.3;
		double Sat = 21.7;
		double Sun = 18.9;

		System.out.println(Tue - Mon);
		System.out.println(Wed - Tue);
		System.out.println(Thu - Wed);
		System.out.println(Fri - Thu);
		System.out.println(Sat - Fri);
		System.out.println(Sun - Sat);
	}
}